    public function update(Request $request, $id)
    {
        // Ambil data berdasarkan id di tabel user
        $data = User::findOrFail($id);
        // Update data user
        $data->update([
            'name' => $request['name'],
            'email' => $request['email'],
            'role' => $request['role'],
            'password' => Hash::make($request['password']),
        ]);
        // Redirect ke tabel user
        return redirect('user');
    }

